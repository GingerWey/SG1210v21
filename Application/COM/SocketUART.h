// 用于UART的Socket对象
class TCOMSocketUART : public TCOMSocket
{

public:
  // 构造
  TCOMSocketUART() : TCOMSocket() 
    { 
    StateSet(sksConn | sksSentOk); 
    Init(); 
    }

  // 读写方法
  // pucBuffer: 接收缓冲区
  //  uwBufLen: pucBuffer的长度
  // 返回：实际读写的字节数
  virtual uint32_t Read ( void *pvBuffer, uint32_t uBufLen );
  virtual uint32_t Write( const void* pvBuffer, 
                            uint32_t  uNumBytes,
                            uint32_t  uDestAddr = 0,
                            uint32_t  uDestPort = 0 );

  // 取未接收的报文长度
  uint32_t RevNumBytes( void );
};
